// Add classes when elements become visible through scolling (provided by js/lib/jquery.visible.min.js): CJDM, 12/12/13
// With thanks to Chris Coyier: http://css-tricks.com/slide-in-as-you-scroll-down-boxes/

var win = $(window);
var allMods = $(".module");

// Already visible modules
allMods.each(function(i, el) {
  var el = $(el);
  if (el.visible(true)) {
    el.addClass("already-visible"); 
  } 
});

win.scroll(function(event) {
  
  allMods.each(function(i, el) {
    var el = $(el);
    if (el.visible(true)) {
      el.addClass("come-in"); 
    } 
  });
  
});

